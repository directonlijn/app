<strong><em>Lees deze info goed!</em></strong><br>
Hippiemarkt The Beach Aalsmeer is onderdeel van Direct Events. Hippiemarkt The Beach Aalsmeer zal op vrijdag van 17:00 tot 23:00 duren, zaterdag van 12:00 tot 20:00 en zondag van 12:00 tot 18:00. Er is beperkt ruimte voor maximaal 50  kramen die aanwezig zijn met verschillende producten in het Ibiza thema. Ook zal er een kidscorner aanwezig zijn voor de kinderen. De indoor Hippiemarkt zal plaatsvinden tijdens de world tour beachvolleybal. Hier komen ruim 64 teams uit verschillende landen op af. In de beach wordt de gehele dag gezorgd voor entertainment (dj’s, , workshops en meer). Wij maken gebruik van 4 meter kramen.<br>
<br>
<strong>LOCATIE:</strong><br>
<strong>Adres: </strong>The Beach Aalsmeer, Oosteinderweg 247, 1432 AT Aalsmeer<br>
<strong>Opbouw: </strong>vanaf vrijdag 14:00 (indien u alle dagen of de vrijdag geboekt heeft)<br>
<strong>Afbouw: </strong>U kunt uw producten achterlaten. De ruimte wordt na de markt afgesloten<br>
<br>
